// Кристаллы — мелкие коллекционные точки, разбросанные по карте.
// В отличие от основных точек (poi-data.ts):
//   - не требуют dwell-таймера, подбираются мгновенно вблизи;
//   - видны на карте игроку только когда он ближе 1 км (см. CrystalsService);
//   - дают донат-валюту «кристаллы», а не баллы опыта/монеты.
//
// ЧЕСТНО О ТЕКУЩЕМ НАБОРЕ: ниже — стартовый демонстрационный набор
// (24 штуки, по 3 рядом с каждой из 8 подтверждённых точек), координаты
// рассчитаны как смещения от известных точек, а НЕ взяты с реальных троп
// (у нас нет доступа к точным данным о тропах). Для полного покрытия всей
// области из расчёта «1 кристалл на 1 кв. км, преимущественно на дорогах»
// в будущем нужна отдельная задача с выгрузкой реальных дорог/троп из
// OpenStreetMap (Overpass API) и расстановкой точек вдоль них — это большая
// отдельная работа, не эквивалент ручного добавления через этот файл.
//
// КАК ДОБАВИТЬ СВОЙ КРИСТАЛЛ ВРУЧНУЮ — скопируй и заполни:
//
//   { lat: 55.1234, lng: 60.5678, reward: 5 },
//
// lat/lng бери из Яндекс/Google Карт (клик правой кнопкой → координаты).
// reward — сколько кристаллов получит игрок (обычно 3-10).
// ============================================================

